class GenDataProcessor(DataProcessor):
    """Processor for generated data set"""

    def _read_json(self, file_dir):
        data_dict = json.load(open(file_dir, 'r'))
        return data_dict

    def _create_examples_from_json(self, data_dict, set_type="train"):
        examples = []
        for (i, data) in enumerate(data_dict):
            guid = "%s-%s" % (set_type, i)
            if "text1" in data:
                text_a = data["text1"]
                text_b = data["text2"]
            else:
                text_a = data["text"]
                text_b = None
            label = data["label"]
            examples.append(InputExample(guid=guid, text_a=text_a, text_b=text_b, label=label))
        return examples


class MrpcProcessor(GenDataProcessor):
    """Processor for the MRPC data set (GLUE version)."""

    def get_example_from_tensor_dict(self, tensor_dict):
        """See base class."""
        return InputExample(
            tensor_dict["idx"].numpy(),
            tensor_dict["sentence1"].numpy().decode("utf-8"),
            tensor_dict["sentence2"].numpy().decode("utf-8"),
            str(tensor_dict["label"].numpy()),
        )

    def get_train_examples(self, data_dir):
        """See base class."""
        return self._create_examples_from_json(self._read_json(os.path.join(data_dir, "train.json")))

    def get_test_examples(self, data_dir):
        """See base class."""
        return self._create_examples(self._read_tsv(os.path.join(data_dir, "test.tsv")), "test")

    def get_labels(self):
        """See base class."""
        return ["0", "1"]

    def _create_examples(self, lines, set_type):
        """Creates examples for the training, dev and test sets."""
        examples = []
        for (i, line) in enumerate(lines):
            if i == 0:
                continue
            guid = "%s-%s" % (set_type, i)
            text_a = line[3]
            text_b = line[4]
            label = line[0]
            examples.append(InputExample(guid=guid, text_a=text_a, text_b=text_b, label=label))
        return examples


